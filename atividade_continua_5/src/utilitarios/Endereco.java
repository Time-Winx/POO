package utilitarios;

public class Endereco {

    public String Pais;
    public String Estado;
    public String Cidade;
    public String CEP;
    public String Rua;
    public String Numero;
    public String Complemento;

    public Endereco(String Pais, String Estado, String Cidade, String CEP,
                    String Rua, String Numero, String Complemento) {
        this.Pais = Pais;
        this.Estado = Estado;
        this.Cidade = Cidade;
        this.CEP = CEP;
        this.Rua = Rua;
        this.Numero = Numero;
        this.Complemento = Complemento;
    }

}
